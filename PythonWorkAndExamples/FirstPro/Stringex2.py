print("String Functions")

mystr = "Jitendra is a good boy"
print(mystr)
print("Print the character index :- ",mystr.index("i"))
print("Print the end character is true or false :- ",mystr.endswith("boy"))
print("Counting a character in string :- ",mystr.count("J"))
print("First character is capital :- ",mystr.capitalize())
print("All string in lower case :- ",mystr.lower())
print("All string in upper case",mystr.upper())
print("Replace the is into are :- ",mystr.replace("is", "are"))

