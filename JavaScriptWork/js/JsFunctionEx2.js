function sum(a,b){
    let c = a + b;
    return c;
}

function sub(a,b){
    let c = a - b;
    return c;
}

function mul(a,b){
    let c = a * b;
    return c;
}

function div(a,b){
    let c = a / b;
    return c;
}

let returnvalue1 = sum(10,20)
console.log("Sum of two numbers:- "+ returnvalue1)

let returnvalue2 = sub(10,20)
console.log("Substraction of two numbers:-"+ returnvalue2)

let returnvalue3 = mul(10,20)
console.log("Multiplication of two numbers:-"+ returnvalue3)

let returnvalue4 = div(10,20)
